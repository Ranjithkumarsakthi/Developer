package javaintro;

public class Nest_for {

	public static void main(String[] args) {
		//print star *
		for(int t=1;t<=5;t++) {
			for(int v=1;v<=3;v++)
			{
				System.out.print("* ");
			}
			System.out.println();
		}
		
		///print left triangle *
		for(int k=1;k<=5;k++) {
			for(int l=1;l<=k;l++) {
				System.out.print("*");
			}
			System.out.println();
		}

		
				

	}

}
